var board [50][50]int
var pause bool
var field *ebiten.Image

var whiteSquare *ebiten.Image
var blackSquare *ebiten.Image
var spaceButtonLastPressed int64
var mouseButtonLastPressed int64
var lastEvolve int64

func update(screen *ebiten.Image) error {
	if pause == false {
		evolveTime := time.Now().UnixNano()
		if evolveTime-lastEvolve > 250000000 {
			lastEvolve = evolveTime
			board = Evolve(board)
		}
		// board = Evolve(board)
	}
	screen.Fill(color.RGBA{255, 255, 255, 255})
	field.Fill(color.RGBA{255, 255, 255, 255})

	if ebiten.IsKeyPressed(ebiten.KeySpace) {
		pressedTime := time.Now().UnixNano()
		if pressedTime-spaceButtonLastPressed > 500000000 {
			spaceButtonLastPressed = pressedTime
			if pause == true {
				pause = false
			} else {
				pause = true
			}
		}

	}
	if ebiten.IsMouseButtonPressed(ebiten.MouseButtonLeft) {
		pressedTime := time.Now().UnixNano()
		if pressedTime-mouseButtonLastPressed > 400000000 {
			mouseButtonLastPressed = pressedTime
			mouseX, mouseY := ebiten.CursorPosition()
			x := (mouseX) / 16
			y := (mouseY) / 16
			xInt := int(math.Floor(float64(x)))
			yInt := int(math.Floor(float64(y)))
			if board[xInt][yInt] == 0 {
				board[yInt][xInt] = 1
			} else {
				board[yInt][xInt] = 0
			}

		}
	}
	for x := 0; x < 50; x++ {
		for y := 0; y < 50; y++ {
			opts := &ebiten.DrawImageOptions{}
			opts.GeoM.Translate(float64(x)*16, float64(y)*16)
			if board[y][x] == 1 {
				field.DrawImage(blackSquare, opts)
			} else {
				field.DrawImage(whiteSquare, opts)
			}

		}
	}
	fps := fmt.Sprintf("TPS: %f", ebiten.CurrentTPS())
	ebitenutil.DebugPrint(field, fps)
	screen.DrawImage(field, nil)
	return nil
}

