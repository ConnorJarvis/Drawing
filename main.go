func ListenForSpacebar() {
	for {
		robotgo.AddEvent("space")
		if pause == false {
			pause = true
		} else {
			pause = false
		}
		time.Sleep(time.Millisecond * 200)
	}

}
func EvolveBoard() {
	for {
		time.Sleep(time.Millisecond * 250)
		if pause == false {
			board = Evolve(board)
		}
	}

}

