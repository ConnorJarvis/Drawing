var board [50][50]int
var pause bool

func run() {
	cfg := pixelgl.WindowConfig{
		Title:  "Game of Life",
		Bounds: pixel.R(0, 0, 800, 800),
	}
	win, err := pixelgl.NewWindow(cfg)
	if err != nil {
		panic(err)
	}
	// imd := imdraw.New(nil)
	// color := float64(255)
	// x, y := 1, 1
	// imd.Color = pixel.RGB(0, 0, 0)
	// imd.Push(pixel.V(0, 0))
	// imd.Color = pixel.RGB(0, 0, 0)
	// imd.Push(pixel.V(100, 100))
	// imd.Rectangle(0)
	go EvolveBoard()
	go ListenForSpacebar()
	// go ListenForMouse(win)
	for !win.Closed() {
		if win.JustPressed(pixelgl.MouseButtonLeft) {
			mousePos := win.MousePosition()
			x := (mousePos.X) / 16
			y := (mousePos.Y) / 16
			xInt := int(math.Floor(x))
			yInt := int(math.Floor(y))
			if board[49-yInt][xInt] == 0 {
				board[49-yInt][xInt] = 1
			} else {
				board[49-yInt][xInt] = 0
			}
			time.Sleep(time.Millisecond * 100)
		}
		win.Clear(colornames.White)
		// spew.Dump(board)
		for x := 0; x < 50; x++ {
			for y := 0; y < 50; y++ {

				color := float64(0)
				if board[x][y] == 0 {
					color = 255
				}
				imd := imdraw.New(nil)
				imd.Color = pixel.RGB(color, color, color)
				imd.Push(pixel.V((float64(y) * 16), 800-(float64(x)*16)))
				imd.Color = pixel.RGB(color, color, color)
				imd.Push(pixel.V((float64(y+1) * 16), 800-(float64(x+1)*16)))
				imd.Rectangle(0)
				imd.Draw(win)
			}
		}

		win.Update()
	}

}

